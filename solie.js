const langBtn = document.getElementById('lang-btn');
  const navLinks = document.getElementById("navLinks");
const mainTitle = document.getElementById("mainTitle");
let currentLang = 'ar'; 

// تبديل اللغة
langBtn.addEventListener('click', () => {
    currentLang = (currentLang === 'en') ? 'ar' : 'en';

    // 1. تحديث الاتجاه واللغة
    const direction = (currentLang === 'ar') ? 'rtl' : 'ltr';
    document.documentElement.dir = direction;
    document.documentElement.lang = currentLang;

    // 2. تحديث النصوص
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[currentLang][key]) {
            element.textContent = translations[currentLang][key];
        }
    });

    langBtn.textContent = translations[currentLang].langBtn;

    // 3. إعادة تشغيل الأنيميشن عند تغيير اللغة
    mainTitle.style.animation = 'none';
    mainTitle.offsetHeight; // Trick لإعادة تشغيل الـ Reflow
    mainTitle.style.animation = 'fadeIn 0.8s ease-in-out forwards';

    hideMenu();
});
// وظائف القائمة الجانبية باستخدام classList لضمان السلاسة
function showMenu() {
    navLinks.classList.add("active");
}

function hideMenu() {
    navLinks.classList.remove("active");
}

    const faqs = document.querySelectorAll('.faq-card summary');

    faqs.forEach(summary => {
        summary.addEventListener('click', function(e) {
            e.preventDefault(); // بنمنع المتصفح يفتح ويقفل بمزاجه
            
            const card = this.parentNode;

            if (card.hasAttribute('open')) {
                // لو مفتوح، بنشغل أنيميشن القفل الأول
                card.classList.add('closing');
                
                // بنستنى 400 ملي ثانية (نفس وقت الـ CSS) وبعدين نقفله رسمي
                setTimeout(() => {
                    card.removeAttribute('open');
                    card.classList.remove('closing');
                }, 400);
            } else {
                // اختياري: لو عايزة باقي الأسئلة تقفل لما واحد يتفتح
                document.querySelectorAll('.faq-card[open]').forEach(openCard => {
                    openCard.classList.add('closing');
                    setTimeout(() => {
                        openCard.removeAttribute('open');
                        openCard.classList.remove('closing');
                    }, 400);
                });

                // نفتح الكارت الحالي
                card.setAttribute('open', '');
            }
        });
    });
